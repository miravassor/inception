USE mysql;
FLUSH PRIVILEGES;
DELETE FROM mysql.user WHERE User='';
DROP DATABASE test;
DELETE FROM mysql.db WHERE Db='test';
DELETE FROM mysql.user WHERE User='root' AND Host NOT IN ('localhost', '127.0.0.1', '::1');
ALTER USER 'root'@'localhost' IDENTIFIED BY '${MDBROOTPASSWORD}';
CREATE DATABASE IF NOT EXISTS ${MDBDATABASE} CHARACTER SET utf8 COLLATE utf8_general_ci;
CREATE USER IF NOT EXISTS '${MDBUSER}'@'%' IDENTIFIED by '${MDBPASSWORD}';
GRANT ALL PRIVILEGES ON ${MDBDATABASE}.* TO '${MDBUSER}'@'%';
FLUSH PRIVILEGES;